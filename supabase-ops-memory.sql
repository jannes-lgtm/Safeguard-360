-- ── 5. Detected operational anomalies ───────────────────────────────────────
create table if not exists public.ops_anomalies (
  id             uuid primary key default gen_random_uuid(),
  detected_at    timestamptz not null default now(),
  anomaly_type   text not null,
  -- types: 'feed_degradation' | 'feed_blackout' | 'notification_spike' | 'latency_spike'
  --   | 'escalation_failure' | 'ws_disconnect_spike' | 'regional_blackout'
  --   | 'regional_degradation' | 'api_latency_spike' | 'predictive_warning'
  severity       text not null,  -- 'critical' | 'high' | 'medium' | 'low'
  subject        text,           -- feed_id, region, endpoint name
  description    text,
  metrics        jsonb,
  predictive     boolean default false,  -- true = forward-looking warning
  resolved_at    timestamptz,
  acknowledged   boolean default false,
  acknowledged_by text,
  acknowledged_at timestamptz,
  auto_resolved  boolean default false
);

create index if not exists idx_ops_anomalies_detected    on public.ops_anomalies (detected_at desc);
create index if not exists idx_ops_anomalies_unresolved  on public.ops_anomalies (detected_at desc) where resolved_at is null;
create index if not exists idx_ops_anomalies_severity    on public.ops_anomalies (severity, detected_at desc);
create index if not exists idx_ops_anomalies_subject     on public.ops_anomalies (subject, anomaly_type) where resolved_at is null;

alter table public.ops_anomalies enable row level security;
drop policy if exists "Service role full access" on public.ops_anomalies;
create policy "Service role full access" on public.ops_anomalies for all using (true) with check (true);

