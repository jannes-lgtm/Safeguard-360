-- ── 1. Unified operational event log ────────────────────────────────────────
create table if not exists public.ops_events (
  id           uuid primary key default gen_random_uuid(),
  ts           timestamptz not null default now(),
  event_type   text not null,
  -- event_type values: 'feed_fetch' | 'notification_sent' | 'notification_failed'
  --   | 'retry' | 'api_call' | 'escalation' | 'ws_disconnect' | 'scan_run'
  --   | 'auth_check' | 'chaos_test' | 'anomaly_detected'
  endpoint     text,
  region       text,          -- ISO country code or region slug
  feed_id      text,
  duration_ms  integer,
  success      boolean,
  attempt      integer default 1,
  error_code   text,
  error_msg    text,
  metadata     jsonb,
  req_id       text           -- correlates with _logger.js reqId
);

create index if not exists idx_ops_events_ts           on public.ops_events (ts desc);
create index if not exists idx_ops_events_type_ts      on public.ops_events (event_type, ts desc);
create index if not exists idx_ops_events_feed_ts      on public.ops_events (feed_id, ts desc) where feed_id is not null;
create index if not exists idx_ops_events_region_ts    on public.ops_events (region, ts desc) where region is not null;
create index if not exists idx_ops_events_endpoint_ts  on public.ops_events (endpoint, ts desc) where endpoint is not null;

alter table public.ops_events enable row level security;
drop policy if exists "Service role full access" on public.ops_events;
create policy "Service role full access" on public.ops_events for all using (true) with check (true);

-- ── 2. Feed reliability scores (materialised by ops-analyze cron) ───────────
create table if not exists public.feed_reliability (
  feed_id            text primary key,
  feed_name          text,
  category           text,
  geography          text,
  success_count_24h  integer default 0,
  failure_count_24h  integer default 0,
  success_count_7d   integer default 0,
  failure_count_7d   integer default 0,
  last_success       timestamptz,
  last_failure       timestamptz,
  avg_latency_ms     integer,
  p95_latency_ms     integer,
  reliability_score  numeric(5,4),  -- 0.0000 to 1.0000
  baseline_score     numeric(5,4),  -- 30-day learned baseline
  baseline_sigma     numeric(5,4),  -- learned standard deviation
  status             text default 'unknown',
  -- status: 'healthy' | 'degraded' | 'failing' | 'recovering' | 'unknown'
  consecutive_failures integer default 0,
  updated_at         timestamptz default now()
);

alter table public.feed_reliability enable row level security;
drop policy if exists "Service role full access" on public.feed_reliability;
create policy "Service role full access" on public.feed_reliability for all using (true) with check (true);

-- ── 3. Notification delivery history ────────────────────────────────────────
create table if not exists public.notification_delivery (
  id                uuid primary key default gen_random_uuid(),
  ts                timestamptz not null default now(),
  channel           text not null,         -- 'email' | 'sms' | 'whatsapp'
  notification_type text,                  -- 'sos' | 'missed_checkin' | 'incident' | 'alert' | 'passport' | 'debrief' | 'briefing' | 'invite'
  recipient_hash    text,                  -- SHA-256 hash of email/phone — no PII stored
  region            text,
  success           boolean,
  attempt           integer default 1,
  duration_ms       integer,
  error_code        text,
  provider          text,                  -- 'resend' | 'twilio' | 'twilio_whatsapp'
  is_synthetic      boolean default false  -- true = generated by chaos-test / simulation
);

create index if not exists idx_notif_delivery_ts        on public.notification_delivery (ts desc);
create index if not exists idx_notif_delivery_channel   on public.notification_delivery (channel, ts desc);
create index if not exists idx_notif_delivery_type      on public.notification_delivery (notification_type, ts desc);
create index if not exists idx_notif_delivery_success   on public.notification_delivery (success, ts desc);

alter table public.notification_delivery enable row level security;
drop policy if exists "Service role full access" on public.notification_delivery;
